<!DOCTYPE html>
<html>
<head>
	<title>Hello World</title>
</head>
<body>
	<!-- Mengenal PHP -->
	<!-- Jika dengan menggunakan Pure HTML, maka kita akan memiliki batasan untuk mengontrol apa yang kita tampilkan, namun dengan menggunakan php kita bisa menyesuaikan tampilan sesuai dengan kondisi yang berlaku. -->


	<!-- Menampilkan Hello World -->
	<!-- Kita bisa menggunakan beberapa cara untuk menampilkan teks ke layar, salah satunya menggunakan echo -->

	<?php 
	    /*Kode PHP diawali dengan tag pembuka <?php ... dan tag penutup ?> */
		echo "Hello World!";
		/* echo akan menampilkan nilai */
	?>

	<!-- Code PHP dapat diketik di dalam HTML, dan kode HTML juga bisa diketik di dalam PHP, seperti code PHP yang telah kita buat diatas, code itu termasuk diketik di dalam code HTML -->

	<?php echo "<h3> Ini Contoh Kode HTML di Dalam Kode PHP</h3>"; ?>

	<!-- JANGAN LUPA TITIK KOMA -->

</body>
</html>